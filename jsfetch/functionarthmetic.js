function add(a, b){
  return a + b;
}
console.log(`addition: ${add(1,2)}`);

function sub(a, b){
  return a - b;
}
console.log(`substraction: ${sub(10,3)}`);

function mult(a, b){
  return a * b;
}
console.log(`multiplication: ${mult(20,2)}`);

function div(a, b){
  return a / b;
}
console.log(`division: ${div(14,7)}`);