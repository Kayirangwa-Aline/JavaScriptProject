//square root
console.log(Math.sqrt(144));
//absolute
console.log(Math.abs(-144));
//power -> first value is base then second value is power
console.log(Math.pow(100, 2));
//round -> rounds to the nearest integer
console.log(Math.round(4.05));
//ceil -> rounds up to the nearest integer(for ex: 14.0 it will be 14 then 14.4 it will be 15)
console.log(Math.ceil(-14.4));
//floor -> rounds down to the nearest integer
 console.log(Math.floor(-14.4));
//exponent
console.log(Math.exp(144));
//logarithm, cosine, tan, sine
//PI
//random -> returns a number btn 0-1 (ex: 0.1, 0.2,....., 0.9)
console.log(Math.random(250));
//maximun, minimum
console.log(Math.max(36, 98, 76));
console.log(Math.min(36, 98, 76));
//trunc
console.log(Math.trunc(42.76));
